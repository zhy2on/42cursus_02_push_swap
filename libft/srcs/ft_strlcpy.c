#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	n;

	n = 0;
	if (!dst && !src)
		return (0);
	n = ft_strlen(src);
	if (n + 1 < dstsize)
		ft_memcpy(dst, src, n + 1);
	else if (dstsize)
	{
		ft_memcpy(dst, src, dstsize - 1);
		dst[dstsize - 1] = '\0';
	}
	return (n);
}
